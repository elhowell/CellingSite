			<div class="right">
				<p>Bone marrow transplants have been used for the past 40 years to regenerate the blood and immune systems of patients with leukemia, lymphoma, severe aplastic anemia or inherited metabolic diseases. Unfortunately, the major limitation with allogenic bone marrow transplants is the availability of matched donors.<br>
				  <br>
				  Stem cells from Umbilical Cord Blood (UCB) have emerged as an alternative to bone marrow transplants, providing an easily obtainable and readily available source of treatment. UCB transplants may result in a lower incidence of transplant complications, specifically graft-versus-host disease, common in patients receiving a transplant from an unrelated donor. UCB transplants also have less stringent requirements for donor matching compared to bone marrow transplants, increasing the likelihood that an appropriate donor can be found for patients. Until recently, UCB transplants were limited to pediatric patients due to the low cell stem cell dose. But in 2004, researchers demonstrated that combining stem cells from two UCB units could increase the cell dose to extend this lifesaving hematopoietic treatment to adult patients.<br>
				  <br>
				  In addition to regenerating the blood and immune systems, scientists anticipate that stem cells will be used to replace damaged or diseased tissues and organs. Clinical trials are ongoing to repair scarred or dying heart muscle after a heart attack or during congestive heart failure. On-going research in diabetes is focused on understanding how stem cells might be trained to become the type of pancreatic islet cells that secrete needed insulin. Repair of debilitating spinal cord injuries is also a goal of researchers through the regeneration of neurons, myelin and nerve cells.<br>
				  <br>
				  As basic research continues, researchers hope to learn how cells replicate and give rise to specialized daughter cells, which may provide insight into inborn cell errors that cause birth defects. Understanding cell signaling pathways can also provide clues to how stem cells are able to hone in on the site of injury to initiate repair of damaged or diseased tissues.<br>
				  <br>
				  Harnessing stem cells for use as drug delivery systems is another goal of researchers. Stem cells may be able to bring chemotherapeutic agents directly to the targeted cancerous cells. Stem cells may also be used to generate liver cells or other tissues that can be used in screening new drug candidates for safety in pharmaceutical drug development. Using human cells and/or tissues may provide a better model for toxicology testing than the traditional animal models in use today.<br>
				  <br>
				  Cancer vaccines, a type of adoptive immunotherapy, are in clinical trials for prostrate, breast, ovarian and colorectal cancers. Combining tumor cells from the patient with dendritic cells can lead to a vaccine that will seek out and destroy the cancerous cells. Other adoptive immunotherapies can artificially increase the number of T killer cells (a form of white blood cell) in a cancer patient. This involves collection of patient T cells, ex vivo expansion, then reinfusion to the patient. The result is an increase in the number of T cells and a stronger patient immune response to the cancer. <br>
