				<p>A stem cell is a cell (either adult or embryonic) that is capable of indefinite renewal through cell division and retention of its generic or unspecialized state while at the same time maintaining its potential to give rise to daughter cells of a more specialized type.<br>
			    Classification of stem cells as totipotent, pluripotent and multipotent describes the breadth of the stem cells&rsquo; ability to create specialized cell types. Stem cells represent a continuum or spectrum from embryonic stem cells to adult stem cells. The primary distinguishing factor is plasticity - the stem cell's capacity to differentiate into multiple specialized cell types.</p>
				<p>Totipotent stem cells are known as the &ldquo;master&rdquo; cells of the body because they have the capacity to differentiate into the 216 specialized cell types that comprise the human body plus the placenta. A fertilized egg is an example of a totipotent cell.</p>
				<p>Pluripotent stem cells are highly versatile cells and can give rise to any specialized cell type in the body except those needed to develop a fetus. Embryonic stem cells are pluripotent.</p>
				<p>Multipotent stem cells can give rise to several specialized daughter cells but are limited to the particular tissue, organ or physiological system of origin. For example, hematopoietic stem cells can produce many types of blood cells in the circulatory system but cannot differentiate into a brain cell. Hematopoietic stem cells are an example of adult stem cells and are multipotent. Stem cells from umbilical cord blood are also multipotent based on evidence to date.</p>
